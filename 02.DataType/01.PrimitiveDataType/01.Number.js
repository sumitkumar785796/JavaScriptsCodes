/*
Represents numeric values (integers or floating-point numbers).
*/
//integer
let a=9;
//float
let f=3.142;
console.log(a);// output:9
console.log(f);//output:3.142
//Exponential notation
let y =123e5;
let z=123e-5;
console.log(y);//output:12300000
console.log(z);//output:0.00123
//BigInt
let x = BigInt("123456789012345678901234567890");
console.log(x);//output:123456789012345678901234567890n